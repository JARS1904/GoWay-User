import 'package:flutter/material.dart';
import 'package:http/http.dart' as http;
import 'dart:convert';
import '../models/user.dart';

/// ---------------------------------------------------------------------------
/// [UserListScreen]
/// ---------------------------------------------------------------------------
/// Pantalla principal para la gesti√≥n de usuarios del sistema de transporte.
///
/// Caracter√≠sticas:
/// - Muestra lista paginada de usuarios
/// - Operaciones CRUD completas
/// - Integraci√≥n con API PHP mediante HTTP
/// - Manejo de estados: loading, error, vac√≠o
///
/// Estado:
/// - StatefulWidget para manejo de datos din√°micos
class UserListScreen extends StatefulWidget {
  const UserListScreen({super.key});

  @override
  State<UserListScreen> createState() => _UserListScreenState();
}

/// ---------------------------------------------------------------------------
/// [_UserListScreenState]
/// ---------------------------------------------------------------------------
/// Estado y l√≥gica de la pantalla de usuarios.
///
/// Variables de estado:
/// - users: Listado actual de usuarios
/// - isLoading: Indicador de carga
/// - hasError: Indicador de error
/// - apiUrl: Endpoint de la API (ajustar IP seg√ļn entorno)
class _UserListScreenState extends State<UserListScreen> {
  List<User> users = [];
  bool isLoading = true;
  bool hasError = false;
  final String apiUrl =
      "http://192.168.30.101/GoWay/api/usuarios.php"; // Aseg√ļrate de usar la IP correcta

  @override
  void initState() {
    super.initState();
    _fetchUsers(); // Carga inicial de datos al iniciar
  }

  /// -------------------------------------------------------------------------
  /// [_fetchUsers]
  /// -------------------------------------------------------------------------
  /// Obtiene el listado de usuarios desde la API.
  ///
  /// Flujo:
  /// 1. Activa estado de carga
  /// 2. Realiza petici√≥n GET
  /// 3. Procesa respuesta (√©xito/error)
  /// 4. Actualiza estado UI
  Future<void> _fetchUsers() async {
    setState(() {
      isLoading = true;
      hasError = false;
    });

    try {
      final response = await http.get(Uri.parse(apiUrl));

      if (response.statusCode == 200) {
        final List<dynamic> data = json.decode(response.body);
        setState(() {
          users = data.map((json) => User.fromJson(json)).toList();
          isLoading = false;
        });
      } else {
        throw Exception('Error al cargar usuarios: ${response.statusCode}');
      }
    } catch (e) {
      setState(() {
        hasError = true;
        isLoading = false;
      });
      _showSnackbar('Error: $e');
    }
  }

  /// -------------------------------------------------------------------------
  /// [_addUser]
  /// -------------------------------------------------------------------------
  /// Agrega un nuevo usuario mediante POST.
  ///
  /// Par√°metros:
  /// - user: Objeto User con datos b√°sicos
  /// - password: Contrase√Īa en texto plano (se hashea en backend)
  Future<void> _addUser(User user, String password) async {
    try {
      final response = await http.post(
        Uri.parse(apiUrl),
        headers: {'Content-Type': 'application/json'},
        body: json.encode(
            {'nombre': user.name, 'email': user.email, 'password': password}),
      );

      print('Response status: ${response.statusCode}'); // Debug
      print('Response body: ${response.body}'); // Debug

      if (response.statusCode == 201) {
        // 201 es el c√≥digo para creaci√≥n exitosa
        await _fetchUsers(); // Actualiza la lista
        _showSnackbar('Usuario agregado correctamente');
      } else {
        throw Exception('Error: ${response.statusCode} - ${response.body}');
      }
    } catch (e) {
      _showSnackbar('Error al agregar: $e');
      print('Error detallado: $e'); // Debug
    }
  }

  /// -------------------------------------------------------------------------
  /// [_updateUser]
  /// -------------------------------------------------------------------------
  /// Actualiza un usuario existente mediante PUT.
  ///
  /// Par√°metros:
  /// - user: Objeto User con datos actualizados
  /// - password: Nueva contrase√Īa (opcional)
  Future<void> _updateUser(User user, [String password = '']) async {
    try {
      final response = await http.put(
        Uri.parse(apiUrl),
        headers: {'Content-Type': 'application/json'},
        body: json.encode({
          'id': user.id,
          'nombre': user.name,
          'email': user.email,
          'password': password
        }),
      );

      if (response.statusCode == 200) {
        await _fetchUsers();
        _showSnackbar('Usuario actualizado correctamente');
      } else {
        throw Exception('Error al actualizar usuario: ${response.statusCode}');
      }
    } catch (e) {
      _showSnackbar('Error al actualizar usuario: $e');
    }
  }

  /// -------------------------------------------------------------------------
  /// [_deleteUser]
  /// -------------------------------------------------------------------------
  /// Elimina un usuario mediante DELETE.
  ///
  /// Par√°metros:
  /// - id: ID del usuario a eliminar
  Future<void> _deleteUser(int id) async {
    try {
      final response = await http.delete(
        Uri.parse('$apiUrl?id=$id'),
      );

      if (response.statusCode == 200) {
        await _fetchUsers();
        _showSnackbar('Usuario eliminado correctamente');
      } else {
        throw Exception('Error al eliminar usuario: ${response.statusCode}');
      }
    } catch (e) {
      _showSnackbar('Error al eliminar usuario: $e');
    }
  }

  /// -------------------------------------------------------------------------
  /// [_showSnackbar]
  /// -------------------------------------------------------------------------
  /// Muestra un mensaje temporal en la parte inferior.
  ///
  /// Par√°metros:
  /// - message: Texto a mostrar
  void _showSnackbar(String message) {
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(content: Text(message)),
    );
  }

  /// -------------------------------------------------------------------------
  /// [_showAddUserDialog]
  /// -------------------------------------------------------------------------
  /// Muestra di√°logo modal para agregar nuevo usuario.
  void _showAddUserDialog() {
    final nameController = TextEditingController();
    final emailController = TextEditingController();
    final passwordController = TextEditingController();

    showDialog(
      context: context,
      builder: (context) {
        return AlertDialog(
          title: const Text('Agregar Usuario'),
          content: SingleChildScrollView(
            child: Column(
              mainAxisSize: MainAxisSize.min,
              children: [
                TextField(
                  controller: nameController,
                  decoration: const InputDecoration(labelText: 'Nombre'),
                ),
                TextField(
                  controller: emailController,
                  decoration: const InputDecoration(labelText: 'Email'),
                  keyboardType: TextInputType.emailAddress,
                ),
                TextField(
                  controller: passwordController,
                  decoration: const InputDecoration(labelText: 'Contrase√Īa'),
                  obscureText: true,
                ),
              ],
            ),
          ),
          actions: [
            TextButton(
              onPressed: () => Navigator.pop(context),
              child: const Text('Cancelar'),
            ),
            ElevatedButton(
              onPressed: () async {
                final newUser = User(
                  id: 0, // El ID ser√° asignado por la base de datos
                  name: nameController.text,
                  email: emailController.text,
                );
                await _addUser(newUser, passwordController.text);
                if (mounted) Navigator.pop(context);
              },
              child: const Text('Guardar'),
            ),
          ],
        );
      },
    );
  }

  /// -------------------------------------------------------------------------
  /// [_showEditUserDialog]
  /// -------------------------------------------------------------------------
  /// Muestra di√°logo modal para editar usuario existente.
  ///
  /// Par√°metros:
  /// - user: Usuario a editar
  void _showEditUserDialog(User user) {
    final nameController = TextEditingController(text: user.name);
    final emailController = TextEditingController(text: user.email);
    final passwordController = TextEditingController();

    showDialog(
      context: context,
      builder: (context) {
        return AlertDialog(
          title: const Text('Editar Usuario'),
          content: SingleChildScrollView(
            child: Column(
              mainAxisSize: MainAxisSize.min,
              children: [
                TextField(
                  controller: nameController,
                  decoration: const InputDecoration(labelText: 'Nombre'),
                ),
                TextField(
                  controller: emailController,
                  decoration: const InputDecoration(labelText: 'Email'),
                  keyboardType: TextInputType.emailAddress,
                ),
                TextField(
                  controller: passwordController,
                  decoration: const InputDecoration(
                      labelText: 'Nueva Contrase√Īa (opcional)'),
                  obscureText: true,
                ),
              ],
            ),
          ),
          actions: [
            TextButton(
              onPressed: () => Navigator.pop(context),
              child: const Text('Cancelar'),
            ),
            ElevatedButton(
              onPressed: () async {
                final updatedUser = User(
                  id: user.id,
                  name: nameController.text,
                  email: emailController.text,
                );
                await _updateUser(updatedUser, passwordController.text);
                if (mounted) Navigator.pop(context);
              },
              child: const Text('Actualizar'),
            ),
          ],
        );
      },
    );
  }

  /// -------------------------------------------------------------------------
  /// [_showDeleteDialog]
  /// -------------------------------------------------------------------------
  /// Muestra di√°logo de confirmaci√≥n para eliminar usuario.
  ///
  /// Par√°metros:
  /// - userId: ID del usuario a eliminar
  void _showDeleteDialog(int userId) {
    showDialog(
      context: context,
      builder: (context) {
        return AlertDialog(
          title: const Text('Eliminar Usuario'),
          content:
              const Text('¬ŅEst√°s seguro de que quieres eliminar este usuario?'),
          actions: [
            TextButton(
              onPressed: () => Navigator.pop(context),
              child: const Text('Cancelar'),
            ),
            ElevatedButton(
              onPressed: () async {
                await _deleteUser(userId);
                if (mounted) Navigator.pop(context);
              },
              style: ElevatedButton.styleFrom(backgroundColor: Colors.red),
              child: const Text('Eliminar'),
            ),
          ],
        );
      },
    );
  }

  /// -------------------------------------------------------------------------
  /// [build]
  /// -------------------------------------------------------------------------
  /// Construye la interfaz principal de la pantalla.
  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text('Usuarios GoWay'),
        actions: [
          IconButton(
            icon: const Icon(Icons.refresh),
            onPressed: _fetchUsers,
          ),
        ],
      ),
      body: _buildBody(),
      floatingActionButton: FloatingActionButton(
        onPressed: _showAddUserDialog,
        child: const Icon(Icons.add),
      ),
    );
  }

  /// -------------------------------------------------------------------------
  /// [_buildBody]
  /// -------------------------------------------------------------------------
  /// Construye el cuerpo principal seg√ļn el estado actual.
  ///
  /// Estados posibles:
  /// - Loading: Muestra indicador de carga
  /// - Error: Muestra mensaje de error y bot√≥n de reintento
  /// - Vac√≠o: Muestra mensaje "No hay usuarios"
  /// - Listado: Muestra la lista de usuarios
  Widget _buildBody() {
    if (isLoading) {
      return const Center(child: CircularProgressIndicator());
    }

    if (hasError) {
      return Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            const Text('Error al cargar los usuarios'),
            const SizedBox(height: 20),
            ElevatedButton(
              onPressed: _fetchUsers,
              child: const Text('Reintentar'),
            ),
          ],
        ),
      );
    }

    if (users.isEmpty) {
      return const Center(child: Text('No hay usuarios registrados'));
    }

    return RefreshIndicator(
      onRefresh: _fetchUsers,
      child: ListView.builder(
        itemCount: users.length,
        itemBuilder: (context, index) {
          final user = users[index];
          return Card(
            margin: const EdgeInsets.symmetric(horizontal: 16, vertical: 8),
            child: ListTile(
              leading: CircleAvatar(
                child: Text(user.id.toString()),
              ),
              title: Text(user.name),
              subtitle: Text(user.email),
              trailing: Row(
                mainAxisSize: MainAxisSize.min,
                children: [
                  IconButton(
                    icon: const Icon(Icons.edit, color: Colors.blue),
                    onPressed: () => _showEditUserDialog(user),
                  ),
                  IconButton(
                    icon: const Icon(Icons.delete, color: Colors.red),
                    onPressed: () => _showDeleteDialog(user.id),
                  ),
                ],
              ),
            ),
          );
        },
      ),
    );
  }
}
